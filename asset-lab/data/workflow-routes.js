export const ROUTE_BY_ID = Object.freeze(Object.fromEntries(
  WORKFLOW_ROUTES.map((item) => [item.id, item]),
));

export function routeById(id) {
  return ROUTE_BY_ID[id] || ROUTE_BY_ID.home;
}

export function pathFor(id, params = DEMO_ROUTE_PARAMS) {
  return routeById(id).path.replace(/:([a-zA-Z]+)/g, (_, key) => params[key] || DEMO_ROUTE_PARAMS[key] || key);
}

export function hashFor(id, options = {}) {
  const path = pathFor(id, options.params);
  const query = new URLSearchParams(options.query || {});
  return `#${path}${query.size ? `?${query}` : ''}`;
}
