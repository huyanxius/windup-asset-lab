#!/usr/bin/env node
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourcePath = resolve(root, 'contracts/windup.v1.json');
const jsPath = resolve(root, 'asset-lab/data/generated-contract.js');
const typesPath = resolve(root, 'asset-lab/data/generated-contract.d.ts');
const pyPath = resolve(root, 'server/windup_pipeline/generated_contract.py');
const cocosPath = resolve(root, 'assets/scripts/generated-contract.ts');
const contract = JSON.parse(await readFile(sourcePath, 'utf8'));

if (!/^\d+\.\d+\.\d+$/.test(contract.version) || contract.fps !== 8) {
  throw new Error('Contract requires a semantic version and the product-wide 8 FPS invariant');
}
for (const [key, action] of Object.entries(contract.actions || {})) {
  if (!action.label || !action.type || typeof action.loop !== 'boolean' || !Array.isArray(action.phases) || action.phases.length !== 8) {
    throw new Error(`Action ${key} must define label, type, loop and exactly eight phases`);
  }
}
const generation = contract.generation || {};
if (!generation.routes?.includes(generation.defaultRoute)
  || generation.sheet?.frameCount !== 8
  || generation.sheet?.columns !== 8
  || generation.sheet?.rows !== 1
  || !contract.views[generation.starterPack?.view]
  || !generation.starterPack?.actions?.every((action) => contract.actions[action])) {
  throw new Error('Generation contract must define a valid 8-frame sheet route and starter pack');
}
const cocos = contract.cocos || {};
const runtimeActions = cocos.runtimeActions || {};
if (!/^[a-z0-9][a-z0-9/_-]*$/.test(cocos.resourcePath || '')
  || !Object.keys(runtimeActions).length
  || !generation.starterPack.actions.every((action) => Object.values(runtimeActions).some((profile) => profile.contractAction === action))) {
  throw new Error('Cocos contract must map every starter action to a resource profile');
}
for (const [runtimeAction, profile] of Object.entries(runtimeActions)) {
  if (!contract.actions[profile.contractAction]
    || !/^[a-z][a-z0-9-]*$/.test(profile.assetAction || '')
    || !Array.isArray(profile.frameOrder)
    || !profile.frameOrder.length
    || new Set(profile.frameOrder).size !== profile.frameOrder.length
    || profile.frameOrder.some((frame) => !Number.isInteger(frame) || frame < 1 || frame > 99)) {
    throw new Error(`Cocos runtime action ${runtimeAction} has an invalid frame mapping`);
  }
}

const actionOrder = Object.keys(contract.actions);
const frameNamesFunction = `export function cocosFrameNames(action) {\n` +
  `  const profile = cocosRuntime.actions[action];\n` +
  `  if (!profile) return [];\n` +
  `  return profile.frameOrder.map((frame) => profile.assetAction + '-' + String(frame).padStart(2, '0'));\n` +
  `}\n`;
const js = `// Generated by tools/generate-contract.mjs. Do not edit by hand.\n` +
  `export const CONTRACT_VERSION = ${JSON.stringify(contract.version)};\n` +
  `export const FIXED_FPS = ${contract.fps};\n` +
  `export const viewLabels = ${JSON.stringify(Object.fromEntries(Object.entries(contract.views).map(([key, value]) => [key, [value.label, value.truth]])), null, 2)};\n` +
  `export const actionOrder = ${JSON.stringify(actionOrder)};\n` +
  `export const actionLabels = ${JSON.stringify(Object.fromEntries(actionOrder.map((key) => [key, [contract.actions[key].label, contract.actions[key].type]])), null, 2)};\n` +
  `export const actionLoops = ${JSON.stringify(Object.fromEntries(actionOrder.map((key) => [key, contract.actions[key].loop])), null, 2)};\n` +
  `export const actionFrameCounts = ${JSON.stringify(Object.fromEntries(actionOrder.map((key) => [key, contract.actions[key].phases.length])), null, 2)};\n` +
  `export const generationDefaults = ${JSON.stringify(generation, null, 2)};\n` +
  `export const cocosRuntime = ${JSON.stringify({ resourcePath: cocos.resourcePath, actions: runtimeActions }, null, 2)};\n` +
  frameNamesFunction;

const pyValue = (value) => JSON.stringify(value, null, 4).replace(/true/g, 'True').replace(/false/g, 'False').replace(/null/g, 'None');
const py = `# Generated by tools/generate-contract.mjs. Do not edit by hand.\n` +
  `CONTRACT_VERSION = ${pyValue(contract.version)}\n` +
  `FPS = ${contract.fps}\n` +
  `VIEWS = set(${pyValue(Object.keys(contract.views))})\n` +
  `ACTIONS = set(${pyValue(actionOrder)})\n` +
  `IMAGE_MODELS = ${pyValue(contract.imageModels)}\n` +
  `POSES = ${pyValue(Object.fromEntries(actionOrder.map((key) => [key, contract.actions[key].phases])))}\n` +
  `ACTION_LOOPS = ${pyValue(Object.fromEntries(actionOrder.map((key) => [key, contract.actions[key].loop])))}\n` +
  `GENERATION = ${pyValue(generation)}\n` +
  `COCOS_RUNTIME = ${pyValue({ resourcePath: cocos.resourcePath, actions: runtimeActions })}\n`;

const union = (values) => values.map((value) => JSON.stringify(value)).join(' | ');
const runtimeActionOrder = Object.keys(runtimeActions);
const types = `// Generated by tools/generate-contract.mjs. Do not edit by hand.\n` +
  `export type ViewId = ${union(Object.keys(contract.views))};\n` +
  `export type ActionId = ${union(actionOrder)};\n` +
  `export type CocosRuntimeActionId = ${union(runtimeActionOrder)};\n` +
  `export type ImageModelId = ${union(contract.imageModels)};\n` +
  `export declare const CONTRACT_VERSION: ${JSON.stringify(contract.version)};\n` +
  `export declare const FIXED_FPS: ${contract.fps};\n` +
  `export declare const viewLabels: Record<ViewId, readonly [string, string]>;\n` +
  `export declare const actionOrder: readonly ActionId[];\n` +
  `export declare const actionLabels: Record<ActionId, readonly [string, string]>;\n` +
  `export declare const actionLoops: Record<ActionId, boolean>;\n` +
  `export declare const actionFrameCounts: Record<ActionId, number>;\n` +
  `export declare const generationDefaults: { readonly routes: readonly ('sheet' | 'frames')[]; readonly defaultRoute: 'sheet' | 'frames'; readonly sheet: { readonly frameCount: 8; readonly columns: 8; readonly rows: 1 }; readonly starterPack: { readonly view: ViewId; readonly actions: readonly ActionId[] } };\n` +
  `export declare const cocosRuntime: { readonly resourcePath: string; readonly actions: Record<CocosRuntimeActionId, { readonly contractAction: ActionId; readonly assetAction: string; readonly frameOrder: readonly number[] }> };\n` +
  `export declare function cocosFrameNames(action: CocosRuntimeActionId): string[];\n`;

const cocosTypes = `// Generated by tools/generate-contract.mjs. Do not edit by hand.\n` +
  `export const CONTRACT_VERSION = ${JSON.stringify(contract.version)} as const;\n` +
  `export const FIXED_FPS = ${contract.fps} as const;\n` +
  `export const ACTION_IDS = ${JSON.stringify(actionOrder)} as const;\n` +
  `export type ActionId = typeof ACTION_IDS[number];\n` +
  `export const ACTION_LOOPS: Record<ActionId, boolean> = ${JSON.stringify(Object.fromEntries(actionOrder.map((key) => [key, contract.actions[key].loop])), null, 2)};\n` +
  `export const ACTION_FRAME_COUNTS: Record<ActionId, number> = ${JSON.stringify(Object.fromEntries(actionOrder.map((key) => [key, contract.actions[key].phases.length])), null, 2)};\n` +
  `export const COCOS_RESOURCE_PATH = ${JSON.stringify(cocos.resourcePath)} as const;\n` +
  `export const COCOS_RUNTIME_ACTIONS = ${JSON.stringify(runtimeActions, null, 2)} as const;\n` +
  `export type CocosRuntimeActionId = keyof typeof COCOS_RUNTIME_ACTIONS;\n` +
  `export function isActionId(value: string): value is ActionId {\n` +
  `  return (ACTION_IDS as readonly string[]).includes(value);\n` +
  `}\n` +
  `export function cocosFrameNames(action: CocosRuntimeActionId): string[] {\n` +
  `  const profile = COCOS_RUNTIME_ACTIONS[action];\n` +
  `  return profile.frameOrder.map((frame) => profile.assetAction + '-' + String(frame).padStart(2, '0'));\n` +
  `}\n`;

function normalizeNewlines(value) {
  return value.replaceAll('\r\n', '\n');
}

async function sync(path, content) {
  if (process.argv.includes('--check')) {
    const existing = await readFile(path, 'utf8').catch(() => '');
    if (normalizeNewlines(existing) !== normalizeNewlines(content)) {
      throw new Error(`Generated contract is stale: ${path}`);
    }
    return;
  }
  await writeFile(path, content);
}

await Promise.all([sync(jsPath, js), sync(typesPath, types), sync(pyPath, py), sync(cocosPath, cocosTypes)]);
