#!/usr/bin/env node
import { readFile, readdir } from 'node:fs/promises';
import { extname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(fileURLToPath(new URL('..', import.meta.url)));
const assetRoot = join(root, 'asset-lab');
const errors = [];

export function toPosixPath(path) {
  return path.replaceAll('\\', '/');
}

export function relativePosix(from, to) {
  return toPosixPath(relative(from, to));
}

function importsLayer(specifier, layer) {
  return toPosixPath(specifier).split('/').includes(layer);
}

export function findFrontendBoundaryViolations(relativePath, source) {
  const path = toPosixPath(relativePath).replace(/^\.\//, '');
  const imports = [...source.matchAll(/from\s+['"]([^'"]+)['"]/g)].map((match) => match[1]);
  const violations = [];

  if (path.startsWith('core/') && imports.some((value) => importsLayer(value, 'pages') || importsLayer(value, 'features'))) {
    violations.push('core may not depend on pages or features');
  }
  if (path.startsWith('features/') && imports.some((value) => importsLayer(value, 'pages'))) {
    violations.push('features may not depend on pages');
  }
  if (source.includes('fetch(') && path !== 'core/api-client.js') {
    violations.push('HTTP calls must go through core/api-client.js');
  }
  if (source.includes('setInterval(') && path !== 'core/playback-clock.js') {
    violations.push('animation intervals must be owned by PlaybackClock');
  }
  if (/127\.0\.0\.1:517[34]/.test(source) && path !== 'core/runtime-config.js') {
    violations.push('runtime origins must be resolved by core/runtime-config.js');
  }

  return violations;
}

async function files(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const nested = await Promise.all(entries.map((entry) => {
    const path = join(directory, entry.name);
    return entry.isDirectory() ? files(path) : [path];
  }));
  return nested.flat();
}

function fail(file, rule) {
  errors.push(`${relativePosix(root, file)}: ${rule}`);
}

async function main() {
  const javascript = (await files(assetRoot)).filter((file) => ['.js', '.mjs'].includes(extname(file)));
  for (const file of javascript) {
    const source = await readFile(file, 'utf8');
    const path = relativePosix(assetRoot, file);
    for (const violation of findFrontendBoundaryViolations(path, source)) {
      fail(file, violation);
    }
  }

  const appPath = join(root, 'server/app.py');
  const app = await readFile(appPath, 'utf8');
  if (app.split('\n').length > 220) fail(appPath, 'HTTP adapter exceeds 220 lines; move use cases into application services');
  for (const forbidden of ['from .windup_pipeline import config', 'JobStore(', 'generate.gen_', 'processing.']) {
    if (app.includes(forbidden)) fail(appPath, `HTTP adapter contains application/infrastructure logic: ${forbidden}`);
  }

  const backend = (await files(join(root, 'server/windup_pipeline'))).filter((file) => extname(file) === '.py');
  for (const file of backend) {
    const source = await readFile(file, 'utf8');
    if (/config\.API_KEY\s*=/.test(source)) fail(file, 'provider credentials must never mutate global config');
  }

  for (const file of [
    join(root, 'asset-lab/data/generated-contract.js'),
    join(root, 'asset-lab/data/generated-contract.d.ts'),
    join(root, 'server/windup_pipeline/generated_contract.py'),
  ]) {
    const source = await readFile(file, 'utf8');
    if (!source.includes('Generated by tools/generate-contract.mjs')) fail(file, 'generated contract header is missing');
  }

  if (errors.length) {
    console.error(`Architecture boundary check failed:\n- ${errors.join('\n- ')}`);
    process.exit(1);
  }
  console.log(`Architecture boundaries OK (${javascript.length} frontend modules, ${backend.length} backend modules).`);
}

if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  await main();
}
